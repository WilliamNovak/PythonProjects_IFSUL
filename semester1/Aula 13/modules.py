# Pega numero aleatorio
from random import random
print(random())

# Pega aleatorio da lista
from random import choice
lista = [1,7,3,2,9,5]
print(choice(lista))

# Pega numero alatorio no intervalo
from random import uniform
print(uniform(0,10))

# Pega numero inteiro aleatorio no intervalo
from random import randint
print(randint(0,10))